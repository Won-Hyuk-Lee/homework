package com.kosta.lec;

public class Clac {

	public static void main(String[] args) {
		Clac clac = new Clac();
		System.out.println(Add("Calc","ulator"));
		System.out.println(Sum(100,10));
		System.out.println(clac.Div(2,3));
		System.out.println(clac.Mul(100,10));
	}
	
	public static String Add(String a, String b) {
		String result = a + b;
		return result;
	}
	
	public int Mul(int a, int b) {
		int result = a*b;
		return result;
	}
	
	public float Div(float a, float b) {
		float result = a/b;
		return result;
	}
	
	public static int Sum(int a, int b) {
		int result = a+b;
		return result;
	}
	

}

